# Functie care verifica daca numerele dintr-o lista sunt ordonate crescator sau nu. Stiu ca se poate face cu booleans dar nu mai stiu sintaxa asa ca whatever

def is_ordered(l):
    z=1
    for i in range(1,len(l)):
        if l[i]<=l[i-1]:
            z = 0
    return z

# Function that turns a list of characters into a string

def st(l):
    stri=''
    for i in range(len(l)):
        stri = stri+str(l[i])
    return stri
    
# Function that takes a coupling scheme and turns it into a list such as [[1], [1, 2], [1, 2 ,3] etc ect]. 

def gen(l,m):
    scheme=[l]
    for i in range(len(m)-1,-1,-1):
        if st(l) in st(m[i][0]) or st(l) in st(m[i][1]):
            list_A= l + m[i][1]
            list_B = m[i][0] +  l
            if is_ordered(list_A) == 1:
                l = list_A
            elif is_ordered(list_B) == 1:
                l = list_B
            scheme.append(l)
    return scheme

